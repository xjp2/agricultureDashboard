export type Language = 'en' | 'ms' | 'zh';

export const getTranslation = (lang: Language, key: string, params?: { [key: string]: string | number }) => {
  let text = translations[lang][key] || key; // Fallback to key if translation not found

  if (params) {
    for (const paramKey in params) {
      text = text.replace(`{${paramKey}}`, String(params[paramKey]));
    }
  }
  return text;
};